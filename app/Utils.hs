module Utils where

import           Protolude

(.:) :: (c -> r) -> (a -> b -> c) -> (a -> b -> r)
(.:) = (.) . (.)

(.:.) :: (d -> r) -> (a -> b -> c -> d) -> (a -> b -> c -> r)
(.:.) = (.) . (.:)

(.::) :: (e -> r) -> (a -> b -> c -> d -> e) -> (a -> b -> c -> d -> r)
(.::) = (.) . (.:.)

(.::.) :: (f -> r) -> (a -> b -> c -> d -> e -> f) -> (a -> b -> c -> d -> e -> r)
(.::.) = (.) . (.::)

(.:::) :: (g -> r) -> (a -> b -> c -> d -> e -> f -> g) -> (a -> b -> c -> d -> e -> f -> r)
(.:::) = (.) . (.::.)

(.:::.) :: (h -> r) -> (a -> b -> c -> d -> e -> f -> g -> h) -> (a -> b -> c -> d -> e -> f -> g -> r)
(.:::.) = (.) . (.:::)
